#include<iostream>
int main(){

std::cout<<"Hello World."<<'\n'; 
std::cout<<"I am learning C++ language."<< std::endl;  
std::cout<<"I am preparing for exam.";
return 0;
}

/* 
'<<'    =two left angle brackets aka ' Insertion Operator' or Left Shift Operator  when used with numbers.
'cout'  = console output.(Similar to printf).
'endl'  = endline.
'\n'    = new line (Better than endl performance wise)
'::'    = scope resolution operator. (to access namespace variable)

*/